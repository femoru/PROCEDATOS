public class ArchivoGuiaBeans {
    
    private int idCarga;
    private int idArchivoPlano;
    private int guia;
    private int registros;
    private String fechaRegistro;
    private String usuarioSOS;

    public int getIdCarga() {
        return idCarga;
    }

    public void setIdCarga(int idCarga) {
        this.idCarga = idCarga;
    }

    public int getIdArchivoPlano() {
        return idArchivoPlano;
    }

    public void setIdArchivoPlano(int idArchivoPlano) {
        this.idArchivoPlano = idArchivoPlano;
    }

    public int getGuia() {
        return guia;
    }

    public void setGuia(int guia) {
        this.guia = guia;
    }

    public int getRegistros() {
        return registros;
    }

    public void setRegistros(int registros) {
        this.registros = registros;
    }

    public String getFechaRegistro() {
        return fechaRegistro;
    }

    public void setFechaRegistro(String fechaRegistro) {
        this.fechaRegistro = fechaRegistro;
    }

    public String getUsuarioSOS() {
        return usuarioSOS;
    }

    public void setUsuarioSOS(String usuarioSOS) {
        this.usuarioSOS = usuarioSOS;
    }
    
    
    
    
    
}
